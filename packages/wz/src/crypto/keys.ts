export const WZ_GMS_IV = new Uint8Array([0x4d, 0x23, 0xc7, 0x2b]);
export const WZ_MSEA_IV = new Uint8Array([0xb9, 0x7d, 0x63, 0xe9]);
export const WZ_ZERO_IV = new Uint8Array(4);

/** The offset cipher constant used for directory-entry offset decryption. */
export const WZ_OFFSET_CONSTANT = 0x581c3f6d;

/**
 * Derive the 32-byte AES-256 key from the 128-byte UserKey. Mirrors
 * `getTrimmedUserKey` in @tybys/wz: take every 16th byte (positions
 * 0, 16, 32, ..., 112) and write them to key positions 0, 4, 8, ..., 28.
 * The 24 in-between bytes stay zero.
 */
export function trimUserKey(userKey: Uint8Array = MAPLESTORY_USERKEY): Uint8Array {
  if (userKey.length !== 128) {
    throw new RangeError(`UserKey must be 128 bytes, got ${userKey.length}`);
  }
  const out = new Uint8Array(32);
  for (let i = 0; i < 128; i += 16) {
    out[i / 4] = userKey[i]!;
  }
  return out;
}

export function ivForVersion(version: WzVersion): Uint8Array {
  switch (version) {
    case 'GMS':
      return WZ_GMS_IV;
    case 'EMS':
    case 'MSEA':
      return WZ_MSEA_IV;
    case 'BMS':
    case 'CLASSIC':
      return WZ_ZERO_IV;
  }
}

/** True when the version uses an all-zero IV — i.e. no AES, keystream is all zeros. */
export function isUnencrypted(version: WzVersion): boolean {
  const iv = ivForVersion(version);
  return iv[0] === 0 && iv[1] === 0 && iv[2] === 0 && iv[3] === 0;
}
